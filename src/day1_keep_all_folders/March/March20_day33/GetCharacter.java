package day1_keep_all_folders.March.March20_day33;

public class GetCharacter {
    public static void A_to_Z() {
        for (char i = 'A'; i <= 'Z'; i++) {
            System.out.print(i + " ");

        }
        System.out.println();
    }

    public static void a_TO_z() {
        for (char i = 'a'; i <= 'z'; i++) {
            System.out.print(i + " ");

        }
        System.out.println();
    }

    public static void Z_TO_A() {
        for (char i = 'Z'; i >= 'A'; i--) {
            System.out.print(i + " ");

        }
        System.out.println();
    }

    public static void z_TO_a() {
        for (char i = 'z'; i >= 'a'; i--) {
            System.out.print(i + " ");

        }
        System.out.println();
    }

    public static void num_0_TO_9() {
        for (int i = 0; i <= 9; i++) {
            System.out.print(i + " ");

        }
        System.out.println();
    }

    public static void num_9_TO_0() {
        for (int i = 9; i >= 0; i--) {
            System.out.print(i + " ");
        }
        System.out.println();
    }
}

