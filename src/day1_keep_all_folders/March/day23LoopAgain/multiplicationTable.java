package day1_keep_all_folders.March.day23LoopAgain;

public class multiplicationTable {
    public static void main(String[] args) {
        int num2 = 5;
        int result;

        for (int number = 1; number <= 10; number++) {
            result = number*num2;
            System.out.println(num2 + "x" + number + " = "+result);
}
    }
}