package day1_keep_all_folders.March.day23LoopAgain;

public class LoopFor__Example {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            System.out.print(i + " ");
        }
        int z = 1;
        while (z <= 10) {
            System.out.println(z);
            z++;
        }


    }
}

