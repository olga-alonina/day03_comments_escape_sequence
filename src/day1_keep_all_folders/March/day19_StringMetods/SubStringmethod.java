package day1_keep_all_folders.March.day19_StringMetods;

public class SubStringmethod {
    public static void main(String[] args){
        String day = "Sunday";
        //            012345
        System.out.println(day.substring(1));
        System.out.println(day.substring(3));
        System.out.println(day);
        System.out.println("satar"+day.substring(3));
        System.out.println(day.substring(2, 4));
        System.out.println(day.substring(0,5));
        System.out.println(day.substring(0,6));
    }
}
